
        $(document).ready(function() {
            var x = 0;
            $("img.tubiao").click(function() {
                if (x == 0) {
                    x = 1;
                    a();
                } else if (x == 1) {
                    x = 0;
                    b();
                }
            })

            function a() {
                $("img.tubiao").attr('src', '../resources/img/guanzhu1.png');
            }


            function b() {
                $("img.tubiao").attr('src', '../resources/img/guanzhu2.png');
            }



        })


